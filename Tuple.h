struct Pair
{
    ushort x;
    ushort y;
};

struct Triple
{
    ushort x;
    ushort y;
    ushort z;
};

struct Quad
{
    ushort x;
    ushort y;
    ushort z;
    ushort w;
};

#endif //GLYPHATLAS_TUPLE_H
